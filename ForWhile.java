// For loop can be used with the while loop to achieve the same result.
public class ForWhile {
    public static void main(String[] args) {
    for (int i =1; i <=5; i++) {
        System.out.println(i);
    }

    // While loop version
    int j = 1;
    while (j <= 5) {
        System.out.println(j);
        j++;
    }
 }
}

