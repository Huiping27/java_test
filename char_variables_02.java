// char variables behave like intergers.
class CharDemo2 {
    public static void main(String args[]) {
        char ch1;

        ch1 = 'X';
        System.out.println("ch1 chontains " + ch1); // ch1 is first given the value X.

        ch1++; //increment ch1 
        System.out.println("ch1 is now " + ch1); // now results ch1, the next character in the ASCII(and Unicode)sequence.

    }
}